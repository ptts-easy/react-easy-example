import ActionTypes from './types';

const checkReducer = (state , action) => {
  switch (action.type) {
    case ActionTypes.CLICK_COLORITEM:
      return state.map((todo) => {
        if (todo.id === action.id) {
          return { ...todo, complete: !todo.complete };
        } else {
          return todo;
        }
      });
    default:
      return state;
  }
};

const complateReducer = (state , action) => {
  switch (action.type) {
    case "COMPLETE":
      return state.map((todo) => {
        if (todo.id === action.id) {
          return { ...todo, complete: !todo.complete };
        } else {
          return todo;
        }
      });
    default:
      return state;
  }
};

export default {checkReducer, complateReducer};